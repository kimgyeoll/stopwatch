import UIKit


extension UIColor {
    
    /// 시작 버튼 배경색
    class var startBgColor: UIColor {
        return UIColor(red: 0, green: 0.9, blue: 0, alpha: 0.15)
    }
    /// 중단 버튼 배경색
    class var stopBgColor: UIColor {
        return UIColor(red: 0.9, green: 0, blue: 0, alpha: 0.15)
    }
}
